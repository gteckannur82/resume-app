/// Design System Spacing — 8pt Grid.
/// Mathematical harmony via consistent spacing scale.
abstract final class AppSpacing {
  /// Base unit
  static const double unit = 8;

  /// 4px — micro gaps within tight groups
  static const double xs = 4;

  /// 8px — related element spacing
  static const double sm = 8;

  /// 12px — gutter between columns
  static const double gutter = 12;

  /// 16px — standard internal padding
  static const double md = 16;

  /// 20px — horizontal page margin
  static const double containerPadding = 20;

  /// 24px — section internal spacing
  static const double lg = 24;

  /// 32px — between unrelated sections
  static const double xl = 32;

  /// 40px — large section gaps
  static const double xxl = 40;

  /// 48px — hero spacing
  static const double xxxl = 48;

  /// 64px — top safe-area allowance
  static const double safeTop = 64;
}
