/// Design System Border Radius.
/// Shape language: modern, approachable.
/// Cards use lg (16), main containers use xl (20),
/// inputs/buttons use sm (8) or md (12).
abstract final class AppRadius {
  /// 4px — small elements
  static const double sm = 4;

  /// 8px — inputs, small buttons
  static const double base = 8;

  /// 12px — buttons, interactive elements
  static const double md = 12;

  /// 16px — standard cards
  static const double lg = 16;

  /// 20px — main containers, bottom sheets
  static const double xl = 20;

  /// 24px — large modals
  static const double xxl = 24;

  /// 9999px — pills, chips, avatars
  static const double full = 9999;
}
